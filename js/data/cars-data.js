function calculatePricePerDay(price) {
    let finalCarPrice = 0;
    let carPrice = price;
    // let carPrice = allCars[id].price;
  
    if (carPrice >= 60000) {
      finalCarPrice = carPrice * 0.0025;
    } else if (carPrice >= 40000) {
      finalCarPrice = carPrice * 0.0028;
    } else {
      finalCarPrice = carPrice * 0.0024;
    }
  
    return finalCarPrice.toFixed(2);
  }
  